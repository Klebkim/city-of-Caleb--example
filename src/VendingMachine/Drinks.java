package VendingMachine;

public class Drinks {
    private String drinkname;
    private String drinkprice;
    private String drinkquantity;

    public String getDrinkname() {return this.drinkname;}
    public void setDrinkname(String drinkname) {
        this.drinkname = drinkname;
    }
    public String getDrinkprice() {return this.drinkname;}
    public void setDrinkprice(String drinkprice) {
        this.drinkprice = drinkprice;
    }
    public String getDrinkquantity() {return this.drinkquantity;}
    public void setDrinkquantity(String drinkquantity) {
        this.drinkquantity = drinkquantity;
    }
}
