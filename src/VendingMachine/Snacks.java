package VendingMachine;

public class Snacks {
    private String snackname;
    private String snackprice;
    private String snackquantity;

    public String getSnackname() {return this.snackname;}
    public void setSnackname(String snackname) {
        this.snackname = snackname;
    }
    public String getSnackprice() {return this.snackprice;}
    public void setSnackprice(String snackprice) {
        this.snackprice = snackprice;
    }
    public String getSnackquantity() {return this.snackquantity;}
    public void setSnackquantity(String snackquantity) {
        this.snackquantity = snackquantity;
    }
}
